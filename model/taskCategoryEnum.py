"""
This file contains the TaskCategory Enum class. This class	
is used to represent the categories of tasks that can be allocated.
"""	

from enum import Enum

class TaskCategory(Enum):
    UNIVERSITY = 1
    WORK = 2
    HEALTH = 3
    SOCIAL = 4
    FAMILY = 5
    HOBBIES = 6
    MISCELLANEOUS = 7
    

